console.log(`Program for print 5 to 15 by incrementing 1`);
result = " ";
for(i=5;i<=15;i++){
    result = result.concat(i).concat(" ");
}
console.log(result,"\n---------------------------------------------");
console.log(`Program for print 50 to 40 by decrementing 1`);
result = " ";
for(i=50;i>=40;i--){
    result = result.concat(i).concat(" ");
}
console.log(result,"\n---------------------------------------------");
